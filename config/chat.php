// Названия тарифов — по канону (nadezhda-vpn-canon.md): «Для себя» / «Для семьи»
$tariffLines = '';
foreach (['solo' => 'Для себя', 'family' => 'Для семьи'] as $planKey => $planTitle) {
    $plan = $payments['products'][$planKey] ?? null;
    if ($plan === null) {
        continue;
    }
    $rows = [];
    $baseMonthly = null;
    foreach ($plan['rows'] as $period => $row) {
        $months = max(1, (int) round($row['days'] / 30));
        if ($months === 1) {
            $baseMonthly = (int) $row['amount_rub'];
        }
        $extra = '';
        if ($months > 1 && $baseMonthly !== null) {
            $perMonth = (int) round($row['amount_rub'] / $months);
            $savings = $baseMonthly * $months - (int) $row['amount_rub'];
            $extra = ", это ≈{$perMonth} ₽/мес, выгода {$savings} ₽";
        }
        $rows[] = "{$period} — {$row['amount_rub']} ₽, {$row['quota_gb']} ГБ трафика{$extra}";
    }
    $tariffLines .= "- «{$planTitle}» (до {$plan['devices']} устройств): ".implode('; ', $rows).".\n";
}

